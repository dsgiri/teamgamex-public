 AS PERMISSIVE
 FOR ALL
 USING ((EXISTS ( SELECT 1
   FROM (user_roles ur
     JOIN role_master rm ON ((ur.role_id = rm.id)))
  WHERE ((ur.user_id = clerk_user_id()) AND (rm.zone = 'internal'::text)))));


