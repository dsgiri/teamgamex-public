-- DROP FUNCTION tgame.clerk_user_id();

CREATE OR REPLACE FUNCTION tgame.clerk_user_id()
 RETURNS text
 LANGUAGE sql
 STABLE
AS $function$
  SELECT nullif(current_setting('request.jwt.claims', true)::json->>'sub', '')::text;
$function$
;

-- DROP FUNCTION tgame.update_modified_column();

CREATE OR REPLACE FUNCTION tgame.update_modified_column()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
    NEW.dt_modified = NOW();
    RETURN NEW;
END;
$function$
;